colors1 = ["blue", "green", "yellow"]
colors2 = ["red", "pink", "black"]

# combine both lists
colors3 = colors1 + colors2
print(colors3)


# you can check if an item is in a list with the keyword in:
print("green" in colors3)

#using the keyword not to check if an item is not in a list.

print("periwinkle" not in colors3)


# you can get the size (the number of items in it) of a list with the len function
print(len(colors3))
